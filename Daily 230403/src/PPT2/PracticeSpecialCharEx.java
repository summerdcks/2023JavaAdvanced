package PPT2;

public class PracticeSpecialCharEx {	//클래스 선언

	public static void main(String[] args) {	//메인함수 선언
		System.out.println('\'');	//역슬래쉬 뒤의 문자' 출력
		System.out.println("abc\t123\b456");	//\t는 탭, \b는 백스페이스
		System.out.println('n');	//문자 n 출력
		System.out.println("\"Hello\"");	//역슬래쉬 뒤의 문자" 출력
		System.out.println("c:\\");		//역슬래쉬 뒤의 문자\ 출력
	}
}
